public class WhileLoop {

    public static void main(String[] args) {

        System.out.println("_____________while loops________________");
        int a=0;//initiation
        while (a<10) {//condition
            System.out.println("a is less than 10: " +a);
            a++;//iteration
        }
        System.out.println("______For Loops_______");
      //for loops
        for(int i=0; i<10; i++){
            System.out.println("a is less than 10: " +a);
        }
        System.out.println("_____________Do While Loops_____________");
        int b=0;
        do{
            System.out.println("b");
        }while(true);


    }
}





